/*
 * The class for the only fragment in the app, which contains the progress bar,
 * recyclerView, and performs the network calls to the NY Times API.
 */
class MovieListFragment : Fragment() {

    /*
     * Constructing the view
     */
    override fun onCreateView(
        inflater: LayoutInflater, container: ViewGroup?,
        savedInstanceState: Bundle?
    ): View? {
        val view = inflater.inflate(R.layout.fragment_movie_list, container, false)
        val progressBar = view.findViewById<View>(R.id.progress) as ContentLoadingProgressBar
        val recyclerView = view.findViewById<View>(R.id.list) as RecyclerView
        val context = view.context
        recyclerView.layoutManager = GridLayoutManager(context, 2)
        updateAdapter(progressBar, recyclerView)
        return view
    }

    /*
     * Updates the RecyclerView adapter with new data.  This is where the
     * networking magic happens!
     */
    private fun updateAdapter(progressBar: ContentLoadingProgressBar, recyclerView: RecyclerView) {
        progressBar.show()

        // Create and set up an AsyncHTTPClient() here
        val client = AsyncHttpClient()
        val requestParams = RequestParams()
        requestParams["api_key"] = API_KEY

        // Using the client, perform the HTTP request
        client["https://api.themoviedb.org/3/movie/now_playing", requestParams, object :
            JsonHttpResponseHandler() {
            /*
             * The onSuccess function gets called when
             * HTTP response status is "200 OK"
             */
            override fun onSuccess(
                statusCode: Int,
                headers: Headers,
                json: JsonHttpResponseHandler.JSON
            ) {
                // The wait for a response is over
                progressBar.hide()

                val models : MutableList<Movie> = mutableListOf()
                // Filtering the JSON into a parsable string
                val resultsJSON : JSONArray = (json.jsonObject.get("results") as JSONArray)
                for (i in 0 until resultsJSON.length()) {
                    val movie = Movie()
                    val item = resultsJSON.getJSONObject(i)
                    movie.title = item.get("title").toString()
                    movie.image = item.get("poster_path").toString()
//                    Log.d("MovieListFragment", movie.image!!)
                    movie.description = item.get("overview").toString()
                    models.add(movie)
                }
                recyclerView.adapter = MovieListRecyclerViewAdapter(models)

                // Look for this in Logcat:
                Log.d("MovieListFragment", "response successful")
            }

            /*
             * The onFailure function gets called when
             * HTTP response status is "4XX" (eg. 401, 403, 404)
             */
            override fun onFailure(
                statusCode: Int,
                headers: Headers?,
                errorResponse: String,
                t: Throwable?
            ) {
                // The wait for a response is over
                progressBar.hide()

                // If the error is not null, log it!
                t?.message?.let {
                    Log.e("MovieListFragment", errorResponse)
                }
            }
        }]

    }
}
